drop table if exists BOOKS;
drop table if exists PUBLISHERS;
create table PUBLISHERS (ID int AUTO_INCREMENT, NAME VARCHAR(100), primary key (ID));
create table BOOKS (ID int AUTO_INCREMENT, PUBLISHER_ID int, ISBN VARCHAR(20), TITLE VARCHAR(256), PRICE FLOAT , primary key (ID));
